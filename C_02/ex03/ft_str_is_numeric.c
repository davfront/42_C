int	ft_str_is_numeric(char *str)
{
	int	result;
	int	i;

	result = 1;
	i = 0;
	while (str[i] != '\0' && result == 1)
	{
		if (!(str[i] >= '0' && str[i] <= '9'))
		{
			result = 0;
		}
		i++;
	}
	return (result);
}
